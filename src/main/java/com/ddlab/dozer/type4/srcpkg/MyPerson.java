package com.ddlab.dozer.type4.srcpkg;

/**
 * The Class MyPerson is used as a source bean for MyPerson which contains many
 * fields. This is a flat structure bean.
 * 
 * @author <a href="mailto:debadatta.mishra@gmail.com">Debadatta Mishra</a>
 * @since 2013
 */
public class MyPerson {

	/** The name. */
	private String name;

	/** The age. */
	private int age;

	/** The door no. */
	private int doorNo;

	/** The st name. */
	private String stName;

	/** The cname. */
	private String cname;

	/** The ccode. */
	private String ccode;

	/** The fd. */
	private float fd;

	/**
	 * Gets the fd.
	 * 
	 * @return the fd
	 */
	public float getFd() {
		return fd;
	}

	/**
	 * Sets the fd.
	 * 
	 * @param fd
	 *            the new fd
	 */
	public void setFd(float fd) {
		this.fd = fd;
	}

	/**
	 * Gets the cname.
	 * 
	 * @return the cname
	 */
	public String getCname() {
		return cname;
	}

	/**
	 * Sets the cname.
	 * 
	 * @param cname
	 *            the new cname
	 */
	public void setCname(String cname) {
		this.cname = cname;
	}

	/**
	 * Gets the ccode.
	 * 
	 * @return the ccode
	 */
	public String getCcode() {
		return ccode;
	}

	/**
	 * Sets the ccode.
	 * 
	 * @param ccode
	 *            the new ccode
	 */
	public void setCcode(String ccode) {
		this.ccode = ccode;
	}

	/**
	 * Gets the name.
	 * 
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * Sets the name.
	 * 
	 * @param name
	 *            the new name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * Gets the age.
	 * 
	 * @return the age
	 */
	public int getAge() {
		return age;
	}

	/**
	 * Sets the age.
	 * 
	 * @param age
	 *            the new age
	 */
	public void setAge(int age) {
		this.age = age;
	}

	/**
	 * Gets the door no.
	 * 
	 * @return the door no
	 */
	public int getDoorNo() {
		return doorNo;
	}

	/**
	 * Sets the door no.
	 * 
	 * @param doorNo
	 *            the new door no
	 */
	public void setDoorNo(int doorNo) {
		this.doorNo = doorNo;
	}

	/**
	 * Gets the st name.
	 * 
	 * @return the st name
	 */
	public String getStName() {
		return stName;
	}

	/**
	 * Sets the st name.
	 * 
	 * @param stName
	 *            the new st name
	 */
	public void setStName(String stName) {
		this.stName = stName;
	}
}
